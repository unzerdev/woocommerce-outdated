class Customweb_Unzer_Util_Spinner {

	private function __construct(){}

	public static function getLoadOverlayScript(){
		return self::addLoaderCssScript() . self::addOverlayElement() . self::addLoaderElement();
	}

	public static function getRemoveOverlayScript(){
		return "document.getElementsByTagName('body')[0].removeChild(cwOverlay);";
	}

	private static function addOverlayElement(){
		return "var cwOverlay = document.createElement('div'); cwOverlay.setAttribute('style', 'position:fixed;top:0;left:0;width:100%;height:100vh;background-color:rgba(0,0,0,0.3);z-index:999'); document.getElementsByTagName('body')[0].appendChild(cwOverlay);";
	}

	private static function addLoaderElement(){
		return "var cwSpinner = document.createElement('div'); cwSpinner.setAttribute('class', 'cw-loader'); cwSpinner.setAttribute('style', 'top:50%'); cwOverlay.appendChild(cwSpinner);";
	}

	private static function addLoaderCssScript(){
		$css = self::getSpinnerCss();
		return "var cwSpinnerCss = document.createElement('style'); cwSpinnerCss.type = 'text/css'; cwSpinnerCss.appendChild(document.createTextNode(\"$css\")); document.getElementsByTagName('head')[0].appendChild(cwSpinnerCss);";
	}

